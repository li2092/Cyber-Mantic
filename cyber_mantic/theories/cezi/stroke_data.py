def get_stroke_count(char: str) -> int:
    """
    获取汉字笔画数

    Args:
        char: 单个汉字

    Returns:
        笔画数，如果找不到则返回-1
    """
    if len(char) != 1:
        return -1

    return STROKE_COUNT.get(char, -1)


def has_stroke_data(char: str) -> bool:
    """
    检查是否有该汉字的笔画数据

    Args:
        char: 单个汉字

    Returns:
        是否有数据
    """
    if len(char) != 1:
        return False

    return char in STROKE_COUNT
